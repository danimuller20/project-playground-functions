function splitSentence(sentense) {
  let array = [];
  let frase = '';
  for (const index in sentense) {
    if (sentense[index] !== ' ') {
      frase += sentense[index];
    } else if (sentense[index] === ' ') {
      array.push(frase);
      frase = '';
    }
  }
  array.push(frase);
  return array;
}

