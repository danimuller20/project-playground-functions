function splitSentence(string) {
  // seu código aqui 
let array = [];
let word = '';

  for (let contador = 0; contador < string.length; contador += 1) {
    if (string[contador] != '') {
      word += string[contador];
    }else {
      array.push(word);
      word = '';
    }
  }
  array.push(word);
  return array;
}

